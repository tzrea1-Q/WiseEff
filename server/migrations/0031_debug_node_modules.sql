-- Persist debug admin module metadata separately from node.module references.

create table if not exists debug_node_modules (
  id text primary key,
  organization_id text not null references organizations(id),
  name text not null,
  description text not null default '',
  owner text not null default '',
  scope text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (organization_id, name)
);

create index if not exists debug_node_modules_org_name_idx
  on debug_node_modules(organization_id, name);

insert into debug_node_modules (id, organization_id, name, description, owner, scope)
select
  'dmod-' || organization_id || '-' || md5(trim(module)),
  organization_id,
  trim(module),
  '',
  '',
  ''
from debug_nodes
where trim(module) <> ''
group by organization_id, trim(module)
on conflict (organization_id, name) do nothing;
