-- Hierarchical debug node module taxonomy (org-scoped tree).

alter table debug_node_modules
  add column if not exists parent_id text references debug_node_modules(id) on delete restrict,
  add column if not exists path text,
  add column if not exists depth integer,
  add column if not exists sort_order integer not null default 0;

update debug_node_modules
set
  path = id,
  depth = 1
where path is null;

alter table debug_node_modules
  alter column path set not null,
  alter column depth set not null;

alter table debug_node_modules
  drop constraint if exists debug_node_modules_organization_id_name_key;

drop index if exists debug_node_modules_org_name_idx;

create unique index if not exists debug_node_modules_org_parent_name_unique_idx
  on debug_node_modules (organization_id, coalesce(parent_id, ''), name);

create index if not exists debug_node_modules_org_path_idx on debug_node_modules (organization_id, path);
create index if not exists debug_node_modules_org_parent_idx
  on debug_node_modules (organization_id, parent_id, sort_order);

alter table debug_nodes
  add column if not exists debug_node_module_id text references debug_node_modules(id);

update debug_nodes dn
set debug_node_module_id = dm.id
from debug_node_modules dm
where dn.organization_id = dm.organization_id
  and trim(dn.module) = dm.name
  and dm.parent_id is null
  and dn.debug_node_module_id is null;

create index if not exists debug_nodes_debug_node_module_id_idx
  on debug_nodes (organization_id, debug_node_module_id);
